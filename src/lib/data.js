export function getAgenPerubahanFoto(item) {
  if (!item) return "";
  return item.foto || "";
}

export function getAgenPerubahanSorted() {
  return [...agenPerubahan].sort(
    (a, b) => b.tahun - a.tahun || String(a.nama).localeCompare(String(b.nama)),
  );
}

export function getAgenPerubahanById(id) {
  return agenPerubahan.find((item) => Number(item.id) === Number(id)) ?? null;
}

export function getAgenPerubahanInovasiByAgenId(id) {
  return (
    agenPerubahanInovasi.find(
      (item) => Number(item.relatedTo) === Number(id),
    ) ?? null
  );
}

